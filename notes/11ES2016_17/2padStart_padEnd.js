/*
    ''.padStart(final total length of string, padding value)
        - a string method that allow us to pad the start of said string, allowing us to increase the
        length of a string
*/
    
        // Example A: 
            'micheal'.padStart(10) // '   micheal'
            'mikey'.padStart(12,'!') // '!!!!!!!mikey'
            
/*
    ''.padEnd(final total length of string, padding value)
        - similasr to padStart, but at the end of a string
*/
    
        // Example A: 
            'mikey'.padEnd(10,'!') // 'mikey!!!!!'